class RandomMaskingGenerator:
    def __init__(self, input_size=6890, mask_ratio=(0.1, 0.5), mask_prob=0.5):
        
        assert type(mask_ratio) == tuple
        self.mask_ratio = mask_ratio
        self.mask_prob = mask_prob
        self.num_patches = input_size

    def __repr__(self):
        repr_str = "Maks: total patches {}, mask patches {}".format(
            self.num_patches, self.num_mask
        )
        return repr_str

    def __call__(self):
        if self.mask_ratio[0] or self.mask_ratio[1]:
            if random.random() > self.mask_prob:
                self.num_mask = 0
            else:
                assert self.mask_ratio[1] >= self.mask_ratio[0]
                rand_ratio = random.uniform(self.mask_ratio[0], self.mask_ratio[1])
                # rand_ratio = 0.5
                self.num_mask = int(rand_ratio * self.num_patches)

        else: # msak ratio = 0
            self.num_mask = 0

        mask = np.hstack([
            np.zeros(self.num_patches - self.num_mask),
            np.ones(self.num_mask),
        ])
        np.random.shuffle(mask)

        return mask # [196]
    
    
class ConvBNPositionalEncoding(nn.Module):
    """Absolute position embedding with Conv learning.
    Args:
        input_channel (int): input features dim.
        num_pos_feats (int): output position features dim.
            Defaults to 288 to be consistent with seed features dim.
    """

    def __init__(self, input_channel, num_pos_feats=288):
        super().__init__()
        self.position_embedding_head = nn.Sequential(
            nn.Conv1d(input_channel, num_pos_feats, kernel_size=1),
            nn.BatchNorm1d(num_pos_feats), nn.ReLU(inplace=True),
            nn.Conv1d(num_pos_feats, num_pos_feats, kernel_size=1))

    def forward(self, xyz):
        """Forward pass.
        Args:
            xyz (Tensor)ï¼š (B, N, 3) the coordinates to embed.
        Returns:
            Tensor: (B, num_pos_feats, N) the embeded position features.
        """
        xyz = xyz.permute(0, 2, 1)
        position_embedding = self.position_embedding_head(xyz)
        return position_embedding
    
